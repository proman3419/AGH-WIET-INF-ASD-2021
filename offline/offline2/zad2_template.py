from random import randint, seed


class Node:
  def __init__(self):
    self.next = None
    self.value = None
    



def qsort( L ):
  print("Tu proszę napisać swoją funckję")
  return L





def tab2list( A ):
  H = Node()
  C = H
  for i in range(len(A)):
    X = Node()
    X.value = A[i]
    C.next = X
    C = X
  return H.next
  
  
def printlist( L ):
  while L != None:
    print( L.value, "->", end=" ")
    L = L.next
  print("|")

  
  
  

seed(42)

